// Inclusão da biblioteca: #include inclui a biblioteca padrão de entrada e saída, 
// necessária para usar a função printf
#include <stdio.h>

// Função principal: int main() é onde o programa começa a execução. 
// Toda aplicação em C começa pela função main
int main(){
    // Saída de dados: A linha printf("Olá, Mundo!\n"); imprime a mensagem "Olá, Mundo!" 
    // na tela. O \n adiciona uma nova linha ao final da mensagem
    printf("Olá, Mundo!\n");

    // Fim do programa: return 0; indica que o programa terminou com sucesso
    return 0;
}